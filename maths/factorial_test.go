package maths

import "testing"

var facts = []uint64{
	1,                   // 0!
	1,                   // 1!
	2,                   // 2!
	6,                   // 3!
	24,                  // 4!
	120,                 // 5!
	720,                 // 6!
	5040,                // 7!
	40320,               // 8!
	362880,              // 9!
	3628800,             // 10!
	39916800,            // 11!
	479001600,           // 12!
	6227020800,          // 13!
	87178291200,         // 14!
	1307674368000,       // 15!
	20922789888000,      // 16!
	355687428096000,     // 17!
	6402373705728000,    // 18!
	121645100408832000,  // 19!
	2432902008176640000, // 20!
}

func TestFactorial(t *testing.T) {
	for n := range facts {
		n := uint64(n)
		a, b := facts[n], Factorial(n)
		if a != b {
			t.Fatalf("expected %d! = %d, but got %d", n, a, b)
		}
	}
}
